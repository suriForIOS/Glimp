import UIKit
 
extension UIColor {
    static var programDarkBlue: UIColor { return UIColor(red: 11.0/255.0, green: 18.0/255.0, blue: 28.0/255.0, alpha: 1) }
    static var programLightBlue: UIColor { return UIColor(red: 0.0/255.0, green: 255.0/255.0, blue: 255.0/255.0, alpha: 1) }
}
